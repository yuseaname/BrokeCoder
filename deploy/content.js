/**
 * Get related content by tags
 */
export function getRelatedContent(tags, currentSlug, type = 'blog', limit = 3) {
  let content = [];
  
  switch(type) {
    case 'blog':
      content = BLOG_POSTS;
      break;
    case 'career':
      content = CAREERS;
      break;
    case 'skill':
      content = SKILLS;
      break;
    case 'device':
      content = DEVICES;
      break;
  }
  
  return content
    .filter(item => item.slug !== currentSlug)
    .filter(item => item.tags?.some(tag => tags.includes(tag)))
    .slice(0, limit);
}
